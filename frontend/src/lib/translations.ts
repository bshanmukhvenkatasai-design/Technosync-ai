export const triggerGoogleTranslate = (langCode: string) => {
  const targetGoogleLang = googleLanguageMap[langCode] || 'en';
  try {
    if (targetGoogleLang === 'en') {
      // Clear cookies to restore native English rendering
      document.cookie = "googtrans=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/;";
      document.cookie = `googtrans=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/; domain=${window.location.hostname};`;
      document.cookie = "googtrans=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/; domain=localhost;";
      document.cookie = "googtrans=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/; domain=.localhost;";
    } else {
      // Write cookies to force Google Translate language selection state
      document.cookie = `googtrans=/en/${targetGoogleLang}; path=/;`;
      document.cookie = `googtrans=/en/${targetGoogleLang}; path=/; domain=${window.location.hostname};`;
    }

    const googleCombo = document.querySelector('.goog-te-combo') as HTMLSelectElement;
    if (googleCombo) {
      googleCombo.value = targetGoogleLang;
      googleCombo.dispatchEvent(new Event('change'));
    } else {
      setTimeout(() => {
        const retryCombo = document.querySelector('.goog-te-combo') as HTMLSelectElement;
        if (retryCombo) {
          retryCombo.value = targetGoogleLang;
          retryCombo.dispatchEvent(new Event('change'));
        }
      }, 500);
    }
  } catch (e) {
    console.error("Failed to trigger translation:", e);
  }
};
